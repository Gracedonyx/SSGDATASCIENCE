#ACCUMULATOR PATTERN WORKS LIKE GETTING THE TOTAL NUMBER OF THE VARIABLES IN THE LIST
num = [1, 10, 3, 7, 8, 4, 3, 8, 99, 69]
accum = 0    # THIS ASSIGNS THE ACCUMULATOR TO BE ZERO 
for y in num:    #FOR EVERY NUMBER IN num 
    accum = accum + y    # ADD IT ACCUM AND THEN STORE IN ACCUM
print(accum)


